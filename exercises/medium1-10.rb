class Card
  include Comparable
  attr_reader :rank, :suit

  VALUES = { 'Jack' => 11, 'Queen' => 12, 'King' => 13, 'Ace' => 14 }

  def initialize(rank, suit)
    @rank = rank
    @suit = suit
  end

  def to_s
    "#{rank} of #{suit}"
  end

  def value
    VALUES.fetch(rank, rank)
  end

  def <=>(other_card)
    value <=> other_card.value
  end
end

class Deck
  RANKS = ((2..10).to_a + %w(Jack Queen King Ace)).freeze
  SUITS = %w(Hearts Clubs Diamonds Spades).freeze

  def initialize
    reset
  end

  def reset
    @deck = RANKS.product(SUITS).map do |rank, suit|
              Card.new(rank, suit)
            end
    @deck.shuffle!
  end

  def draw
    reset if @deck.empty?
    @deck.pop
  end
end

class PokerHand
  attr_reader :hand

  def initialize(deck)
    @hand = []
    5.times { |_| @hand << deck.draw }
  end

  def print
    hand.each do |card|
      puts card
    end
  end

  def evaluate
    case
    when royal_flush?     then 'Royal flush'
    when straight_flush?  then 'Straight flush'
    when four_of_a_kind?  then 'Four of a kind'
    when full_house?      then 'Full house'
    when flush?           then 'Flush'
    when straight?        then 'Straight'
    when three_of_a_kind? then 'Three of a kind'
    when two_pair?        then 'Two pair'
    when pair?            then 'Pair'
    else                       'High card'
    end
  end

  private


  def royal_flush?
    ranks.sort == [10, 11, 12, 13, 14] && flush?
  end

  def straight_flush?
    flush? && straight?
  end

  def four_of_a_kind?
    ranks.uniq.each do |card|
      return true if ranks.count(card) > 3
    end
    false
  end

  def full_house?
    three_of_a_kind? && two_pair?
  end

  def flush?
    total_suits = {}
    suits.uniq.each do |suit|
      total_suits[suit] = suits.count(suit)
    end
    return true if total_suits.value?(5)
    false
  end

  def straight?
    counter = ranks.sort[0]
    ranks.sort.each do |number|
      return false if counter != number
      counter += 1
    end
    true
  end

  def three_of_a_kind?
    ranks.uniq.each do |card|
      return true if ranks.count(card) > 2
    end
    false
  end

  def two_pair?
    count_pairs > 1
  end

  def pair?
    count_pairs > 0
  end

  def count_pairs
    total_pairs = 0
    ranks.uniq.each do |card|
      if ranks.count(card) > 1
        total_pairs += 1
      end
    end
    total_pairs
  end

  def ranks
    hand_ranks = []
    hand.each do |card|
      hand_ranks << card.value
    end
    hand_ranks
  end

  def suits
    hand_suits = []
    hand.each do |card|
      hand_suits << card.suit
    end
    hand_suits
  end
end

